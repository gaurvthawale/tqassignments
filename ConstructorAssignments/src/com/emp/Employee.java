package com.emp;

public class Employee {
	
	private int emp_id;
	private String name;
	private float sal;
	
	
	public int getEmp_id()
	{
		return emp_id;
	}
	
	public void setEmp_id(int emp_id)
	{
		this.emp_id = emp_id;
	}
	
	public String getName()
	{
		return name;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public float getSal()
	{
		return sal;
	}
	
	public void setSal(float sal) 
	{
		this.sal = sal;
	}
	
	

}
